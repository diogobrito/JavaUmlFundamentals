package br.com.fiap;

public class Pessoa{
	private String nome;
	private String cpf;
	
	public String getNome() {
		return nome;
	}
	
	public String getCPF() {
		return cpf;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
}